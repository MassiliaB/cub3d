#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*dest;
	int		l;

	if (!s)
		return (NULL);
	l = ft_strlen(s);
	if (!(dest = (char *)malloc(sizeof(char) * (l + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
