#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		l;
	char	*dest;

	l = ft_strlen(src);
	if (!(dest = (char *)malloc(sizeof(char) * (l + 1))))
		return (0);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
